package operadores;

public class Aritmeticos {

	public static void main(String[] args) {
		int a = 20;
		int b = 40;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a / b );
		System.out.println(a * b);
		
		double y = 10.5;
		double x = 10.4;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x / y);
		System.out.println(x * y);
		
	
	
	
		int i = 2 + 3 * 3;
		int w = (int) Math.pow(i, 3);
		
		System.out.println(w);
		
		
	}

}
